#!/usr/local/bin/perl

# Script for correcting pk* clones redundancy.
# Requires as input file a list of pk* and corrsponding EMBL:CB* names
# For the format of the input file, see at the end of the script
# usage: perl pk_to_EMBL.pl file_of_names

$|=1;
use Ace;

$db=Ace->connect(-path=>'/nfs/disk100/wormpub/autoace/') or die ("Could not connect\n");

READARRAY: while (<DATA>) {
    chomp $_;
    last READARRAY if $_ =~ /END/;
    push (@PKs,$_);
}
  
foreach (@PKs) {
 chomp $_;
 @fields=split(/\s+/,$_);	
 $pk = $fields[0];
 $pk.=".seq";
 $embl = $fields[1];
 print "\n-D Sequence $pk\n";
 print "-D DNA $pk\n";
 $pkclone = $db->fetch(Sequence=>$pk);
 @homolarray = $pkclone->at('Homol.DNA_homol[1]');
 if ($#homolarray > 0) {	
  foreach (@homolarray) {
	$homol=$_;
 	$homolseq = $db->fetch(Sequence=>$homol);
	my $thisclone=$pkclone;
	$thisclone =~ s/\./\\./mg;
	$myhomol="Homol.DNA_Homol.$thisclone";
 	$homologies = $homolseq->at($myhomol);
	$homoltable=$homologies->asTable;
	$homoltable=~s/$pkclone//;
	@homolrows=split(/\n/,$homoltable);

# Updates the Sequence object for the matched cosmid

	print "\nSequence $homol\n";
	foreach (@homolrows) {
		$currline = $_;
		chomp $currline;
		$currline=~s/^\s+//;
		if ($currline =~ /^(\D+)/) {
		  $match = $1;
		  $currline =~ s/$match//;
		}
                @fields = split (/\s+/,$currline);
		if ($#fields<4) {
		 print " DNA_homol \"$embl\" $match $score $fields[0] $fields[1] $fields[2] $fields[3]\n";   
		} else {
		 $score=$fields[0];
		 print " DNA_homol \"$embl\" $match  $score $fields[1] $fields[2] $fields[3] $fields[4]\n";   
		}
	}
	print "-D DNA_homol $pkclone\n";

# Updates the Sequence object for the EMBL:CB* est

	print "\nSequence $embl\n";
	foreach (@homolrows) {
          $currline = $_;
          chomp $currline;
          $currline=~s/^\s+//;
          if ($currline =~ /^(\D+)/) {
           $match = $1;
 	   $currline=~s/$match//;
          }
	  @fields = split (/\s+/,$currline);
	if ($#fields<4) {
	  print " DNA_homol \"$homol\" $match $score $fields[2] $fields[3] $fields[0] $fields[1]\n";   
	} else {
	   $score=$fields[0];
	   print " DNA_homol \"$homol\" $match $score $fields[3] $fields[4] $fields[1] $fields[2]\n";   
	 }
        }
      }
 }
}

